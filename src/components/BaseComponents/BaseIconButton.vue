<template>
  <span
    class="icon"
    @mouseover="hover = true"
    @mouseleave="hover = false"
    @click="clicked"
  >
    <i :class="classes" />
  </span>
</template>

<script>
  export default {
    name: "BaseIconButton",
    props: {
      icon: {
        type: String,
        required: true
      },
      iconHover: String,
      iconToggle: String,
      iconToggleHover: String,
      toggle: Boolean
    },
    data() {
      return {
        hover: false,
        toggled: false
      };
    },
    computed: {
      classes() {
        if (this.toggle && this.toggled) {
          return [ this.hover ? this.iconToggleHover : this.iconToggle ];
        } else {
          return [ this.hover ? this.iconHover || this.icon : this.icon ];
        }
      }
    },
    methods: {
      clicked(event) {
        if (this.toggle) {
          this.toggled = !this.toggled;
        }

        this.$emit('click', event.target);
      }
    }
  }
</script>

